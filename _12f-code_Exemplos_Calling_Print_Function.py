'''
Exemplos de codigos para chamada à função print.

Esses exemplos ilustram diferentes maneiras de utilizar a função print para exibir
informações na saída do programa. Você pode passar múltiplos argumentos separados
por vírgulas e usar os parâmetros opcionais, como sep (para especificar o separador
entre os argumentos) e end (para definir o caractere final da linha).
 '''

print ('Ana', 'Bia', 'Clô')
#output
#>>>Ana Bia Clô


print ('Ana", Bia', 'Clô', sep=' &' , end= '! ' )
#output
#>>>Ana & Bia & Clô!

print("Olá, mundo!")
#output
#>>>Olá, mundo!


nome = "João"
idade = 25
print("Nome:", nome, "Idade:", idade)
#output
#>>>Nome: João Idade: 25

numero = 10
print("O número é:", numero, "e seu dobro é:", numero * 2)
#output
#>>>O número é: 10 e seu dobro é: 20


lista = [1, 2, 3, 4, 5]
print("A lista contém:", end=" ")
for elemento in lista:
    print(elemento, end=" ")
#output
#>>>A lista contém: 1 2 3 4 5

