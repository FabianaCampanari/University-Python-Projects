##Binary Search is also a method used to locate a specified value in a sorted list.
#It works by repeatedly dividing the list into two halves and discarding the half that
#does not contain the value. It then compares the value with the middle element of the
#remaining half. If they are equal,it returns its position in the list.If the value is
#smaller than the middle element, it repeats the process with the left half.If the value
#is larger than the middle element, it repeats the process with the right half. Binary
#search has a time complexity of O(log n), where n is the length of the list, because
#it reduces the search space by half at each step.

##Linear Search, also known as sequential search, is the simplest search algorithm.
#It searches for a specified value in a list,checking each element in the list.If it
#finds the value, it returns its position in the list. If it does not find the value,
#it returns -1 or some other indicator of failure. Linear search has a time complexity
#of O(n), where n is the length of the list,because it may need to check all n elements
#in the worst case.


 
